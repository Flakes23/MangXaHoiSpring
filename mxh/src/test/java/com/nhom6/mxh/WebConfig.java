/**
 *
 * @author ASUS
 */
@Configuration
public class WebConfig {
    @Bean
    public WebMvcConfigurer corsConfigurer() {
        return new WebMvcConfigurer() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**")
                        .allowedOrigins("http://localhost:5173") // üëà ƒê·ªãa ch·ªâ frontend c·ªßa b·∫°n
                        .allowedMethods("*")
                        .allowedHeaders("*");
            }
        };
    }  
}
